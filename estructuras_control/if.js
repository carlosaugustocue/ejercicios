// Aplicación: Verificar si un número es positivo.
let number = 5;
if (number > 0) {
    console.log("El número es positivo.");
}


// Aplicación: Verificar si un número es par o impar.
let number2 = 4;
if (number2 % 2 == 0) {
    console.log("El número es par.");
} else {
    console.log("El número es impar.");
}

// Aplicación: Clasificar una calificación.
let grade = 85;
if (grade >= 90) {
    console.log("Excelente");
} else if (grade >= 80) {
    console.log("Muy bien");
} else if (grade >= 70) {
    console.log("Bien");
} else if (grade >= 60) {
    console.log("Suficiente");
} else {
    console.log("Insuficiente");
}

